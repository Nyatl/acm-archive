int color[MAX];
ll d[MAX];
ll d2[MAX];

ll dfs(int x) {
	if (color[x]) return d[x];
	color[x] = 1;
	ll res = 1;
	for (auto it : st[x].next) {
		res += dfs(it.second);
	}
	return d[x] = res;
}

ll dfs2(int x) {	
	if (color[x]) return d2[x];
	color[x] = 1;
	ll res = 1;
	for (auto it : st[x].next) {
		if (it.first == X) continue;
		res += dfs2(it.second);
	}
	return d2[x] = res;
}

